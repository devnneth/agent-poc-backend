from __future__ import annotations

from typing import Any
from typing import Literal

from pydantic import BaseModel
from pydantic import Field


# ==================================================================================================
# 소스 메타데이터
# --------------------------------------------------------------------------------------------------
# 원본 문서의 전체적인 특징을 대표하는 요약 정보 모델
# ==================================================================================================
class SourceMetadata(BaseModel):
  topic_keywords: list[str] = Field(default_factory=list, max_length=5)
  description: str
  parser: Literal["opendataloader-pdf"]
  content_format: Literal["markdown"]
  page_count: int | None = None
  has_tables: bool = False
  has_images: bool = False
  image_references: list[ImageReference] = Field(default_factory=list)


# ==================================================================================================
# 이미지 참조 정보
# --------------------------------------------------------------------------------------------------
# 문서 내에 포함된 이미지 파일의 경로 및 캡션 정보를 보관
# ==================================================================================================
class ImageReference(BaseModel):
  alt_text: str
  path: str


# ==================================================================================================
# 청크 상세 정보
# --------------------------------------------------------------------------------------------------
# 개별 청크의 위치, 상위 헤더 등 문서 구조 내 상세 정보를 정의
# ==================================================================================================
class ChunkMetadata(BaseModel):
  heading_path: list[str] = Field(default_factory=list)
  page_range: list[int] | None = None
  content_type: Literal["section", "list", "table", "paragraph"] = "section"


# ==================================================================================================
# 문서 청크 모델
# --------------------------------------------------------------------------------------------------
# 임베딩 및 검색의 기본 단위가 되는 텍스트 조각과 메타데이터 모델
# ==================================================================================================
class DocumentChunk(BaseModel):
  chunk_index: int
  content: str
  metadata: dict[str, Any] | ChunkMetadata = Field(default_factory=dict)

  # ================================================================================================
  # 메타데이터 직렬화
  # ------------------------------------------------------------------------------------------------
  # DB 저장에 사용할 수 있도록 청크 메타데이터를 dict 형태로 반환합니다
  # ================================================================================================
  def metadata_payload(self) -> dict[str, Any]:
    if isinstance(self.metadata, ChunkMetadata):
      return self.metadata.model_dump()
    return dict(self.metadata)


# ==================================================================================================
# 파싱된 문서 결과
# --------------------------------------------------------------------------------------------------
# 다양한 형식의 문서가 변환된 후의 공통 데이터 인터페이스
# ==================================================================================================
class ParsedDocument(BaseModel):
  markdown: str
  document_json: dict[str, Any]
